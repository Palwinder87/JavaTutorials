package day2;

public class L2_DataType {

	public static void main(String[] args) {
		int a=10;
		double d=20.5;
		char c='H';
		boolean b=false;
		// boolean data type will always print value in true and false. 
		// The value initialization has to be done without "".
		
		String s="welcome";
		
		System.out.println(a);
		// This statement will print the value of 'a'.
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(s);

	}

}
